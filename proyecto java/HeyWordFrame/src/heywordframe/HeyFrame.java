    private void JBottonResultadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBottonResultadoActionPerformed
        int operacion = jcdxOperaciones.getSelectedIndex();
        switch (operacion) {

            case 0:
                outexit.setText("Ingresa una operacion");
                txtR.setText(String.valueOf(0));
                txt1.setText(String.valueOf(0));
                txt2.setText(String.valueOf(0));
                break;
            case 1:
                outexit.setText("SUMA");
                if (OCTAL.isSelected() == true) {
                int sum1 = Integer.parseInt(txt1.getText());
                int sum2 = Integer.parseInt(txt2.getText());
                int resutado0 = (sum1 + sum2);
                txtR.setText(String.valueOf(resutado0));
                 
                }
                break;
            case 2:
                outexit.setText("Resta");
                int res1 = Integer.parseInt(txt1.getText());
                int res2 = Integer.parseInt(txt1.getText());
                int resutado1 = (res1 - res2);
                txtR.setText(String.valueOf(resutado1));

                break;
            case 3:
                outexit.setText("Multiplicacion");
                int mul1 = Integer.parseInt(txt1.getText());
                int mul2 = Integer.parseInt(txt1.getText());
                int resutado2 = (mul1 * mul2);
                txtR.setText(String.valueOf(resutado2));

                break;
            case 4:
                outexit.setText("Division");
                int div1 = Integer.parseInt(txt1.getText());
                int div2 = Integer.parseInt(txt1.getText());
                try {
                    int resultado3 = (div1 / div2);
                    txtR.setText(String.valueOf(resultado3));
                } catch (ArithmeticException ex) {
                    outexit.setText("error!!");
                }
                break;
            default:
                txtR.setText("mierda");
                break;
        }

// TODO add your handling code here:
    }//GEN-LAST:event_JBottonResultadoActionPerformed

